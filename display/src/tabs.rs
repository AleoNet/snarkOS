pub(super) struct Tabs {
    pub titles: Vec<&'static str>,
    pub index: usize,
}

impl Tabs {
    pub fn new(titles: Vec<&'static str>) -> Self {
        Self { titles, index: 0 }
    }

    pub fn next(&mut self) {
        self.index = (self.index + 1) % self.titles.len();
    }

    pub fn previous(&mut self) {
        if self.index > 0 {
            self.index -= 1;
        } else {
            self.index = self.titles.len() - 1;
        }
    }
}
